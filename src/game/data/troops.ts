// 获取兵种定义
export function getTroop(id: string): TroopDef | undefined {
  return TROOPS[id];
}

// 获取某建筑可生产的兵种
export function getTroopsByBuilding(building: string): TroopDef[] {
  return Object.values(TROOPS).filter((t) => t.building === building);
}

// 获取已解锁兵种
export function getUnlockedTroops(researchedTechs: string[]): TroopDef[] {
  return Object.values(TROOPS).filter(
    (t) => !t.unlockTech || researchedTechs.includes(t.unlockTech),
  );
}
