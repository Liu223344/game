// 获取超级兵种定义
export function getSuperTroop(id: string): TroopDef | undefined {
  return SUPER_TROOPS[id];
}

// 获取某类别的超级兵种
export function getSuperTroopsByClass(troopClass: string): TroopDef[] {
  return Object.values(SUPER_TROOPS).filter((t) => t.class === troopClass);
}

// 获取已解锁的超级兵种
export function getUnlockedSuperTroops(researchedTechs: string[]): TroopDef[] {
  return Object.values(SUPER_TROOPS).filter(
    (t) => !t.unlockTech || researchedTechs.includes(t.unlockTech),
  );
}
